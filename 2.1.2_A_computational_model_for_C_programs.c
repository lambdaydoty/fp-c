/**
 * A C Program = A list of function declarations: [main, ...]
 * A C Function = A list of statements.
 * A statement is an operation on the `state` of the program.
 */

/**
 * 1) The `return`-statement terminates the executation of a function and returns a result value.
 *
 * 2) The expression-statement evaluates an expression, ignoring the result.
 *
 * 3) The if-statement conditionally executes other statements.
 *
 */

/* A simple C program, it prints `Hello world` */

#include <stdio.h>

int main (void) {
  printf("Hello world\n");
  return 0;
}
